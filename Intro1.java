/*
            Program to find sum of two numbers.
 */
class Intro1
{
	public static void main(String args[])
	{
		int a=10,b=15,sum;
		sum = a+b;
		System.out.println("The sum of a,b is:"+sum);
		if(sum%2 == 0)
			System.out.println("Sum is Even!");
		else
			System.out.println("Sum is Odd!");
	}
}
